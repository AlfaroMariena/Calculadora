public class Numero {
    // Atributo privado que almacena el valor del número.
    private double valor;

    // Constructor que inicializa el atributo 'valor' con el valor proporcionado.
    public Numero(double valor) {
        this.valor = valor; // Asigna el valor recibido al atributo 'valor'.
    }

    // Método público que devuelve el valor del número.
    public double getValor() {
        return valor; // Retorna el valor almacenado.
    }
}
